-- Tenant migration 007: Clinical histories and evolutions

CREATE TABLE IF NOT EXISTS clinical_histories (
    id                                  BIGSERIAL    PRIMARY KEY,
    patient_id                          BIGINT       NOT NULL REFERENCES patients(id) ON DELETE CASCADE,
    created_by                          BIGINT       REFERENCES users(id) ON DELETE SET NULL,
    updated_by                          BIGINT       REFERENCES users(id) ON DELETE SET NULL,
    reason_for_consultation             TEXT,
    current_illness                     TEXT,
    personal_history                    TEXT,
    family_history                      TEXT,
    occupational_history                TEXT,
    uses_optical_correction             BOOLEAN      NOT NULL DEFAULT false,
    optical_correction_type             TEXT,
    last_control_detail                 TEXT,
    ophthalmological_diagnosis          TEXT,
    eye_surgery                         TEXT,
    has_systemic_disease                BOOLEAN      NOT NULL DEFAULT false,
    systemic_disease_detail             TEXT,
    medications                         TEXT,
    allergies                           TEXT,
    -- Visual acuity without correction
    right_far_vision_no_correction      TEXT,
    left_far_vision_no_correction       TEXT,
    right_near_vision_no_correction     TEXT,
    left_near_vision_no_correction      TEXT,
    -- Visual acuity with correction
    right_far_vision_with_correction    TEXT,
    left_far_vision_with_correction     TEXT,
    right_near_vision_with_correction   TEXT,
    left_near_vision_with_correction    TEXT,
    -- External exam
    right_eye_external_exam             TEXT,
    left_eye_external_exam              TEXT,
    right_eye_ophthalmoscopy            TEXT,
    left_eye_ophthalmoscopy             TEXT,
    -- Keratometry
    right_eye_horizontal_k              TEXT,
    right_eye_vertical_k                TEXT,
    left_eye_horizontal_k               TEXT,
    left_eye_vertical_k                 TEXT,
    -- Refraction
    refraction_technique                TEXT,
    right_eye_static_sphere             TEXT,
    right_eye_static_cylinder           TEXT,
    right_eye_static_axis               TEXT,
    right_eye_static_visual_acuity      TEXT,
    left_eye_static_sphere              TEXT,
    left_eye_static_cylinder            TEXT,
    left_eye_static_axis                TEXT,
    left_eye_static_visual_acuity       TEXT,
    right_eye_subjective_sphere         TEXT,
    right_eye_subjective_cylinder       TEXT,
    right_eye_subjective_axis           TEXT,
    right_eye_subjective_visual_acuity  TEXT,
    left_eye_subjective_sphere          TEXT,
    left_eye_subjective_cylinder        TEXT,
    left_eye_subjective_axis            TEXT,
    left_eye_subjective_visual_acuity   TEXT,
    -- Conclusion
    diagnostic                          TEXT,
    treatment_plan                      TEXT,
    observations                        TEXT,
    created_at                          TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at                          TIMESTAMPTZ  NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_clinical_histories_patient_id ON clinical_histories(patient_id);

CREATE TABLE IF NOT EXISTS clinical_evolutions (
    id                      BIGSERIAL    PRIMARY KEY,
    clinical_history_id     BIGINT       NOT NULL REFERENCES clinical_histories(id) ON DELETE CASCADE,
    appointment_id          BIGINT       REFERENCES appointments(id) ON DELETE SET NULL,
    created_by              BIGINT       REFERENCES users(id) ON DELETE SET NULL,
    updated_by              BIGINT       REFERENCES users(id) ON DELETE SET NULL,
    evolution_date          TIMESTAMPTZ,
    subjective              TEXT,
    objective               TEXT,
    assessment              TEXT,
    plan                    TEXT,
    recommendations         TEXT,
    right_far_vision        TEXT,
    left_far_vision         TEXT,
    right_near_vision       TEXT,
    left_near_vision        TEXT,
    right_eye_sphere        TEXT,
    right_eye_cylinder      TEXT,
    right_eye_axis          TEXT,
    right_eye_visual_acuity TEXT,
    left_eye_sphere         TEXT,
    left_eye_cylinder       TEXT,
    left_eye_axis           TEXT,
    left_eye_visual_acuity  TEXT,
    created_at              TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at              TIMESTAMPTZ  NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_clinical_evolutions_history_id ON clinical_evolutions(clinical_history_id);
